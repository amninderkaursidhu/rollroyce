package javacollections;
import java.util.ArrayList;
public class customer {

    public String f, l, u, p, rle;

    public customer(String f, String l, String u, String p, String rle) {
        this.f = f;
        this.l = l;
        this.u = u;
        this.p = p;
        this.rle = rle;

    }

    public String getF() {
        return f;
    }

    public void setF(String f) {
        this.f = f;
    }

    public String getL() {
        return l;
    }

    public void setL(String l) {
        this.l = l;
    }

    public String getU() {
        return u;
    }

    public void setU(String u) {
        this.u = u;
    }

    public String getP() {
        return p;
    }

    public void setP(String p) {
        this.p = p;
    }

    public String getRle() {
        return rle;
    }

    public void setRle(String rle) {
        this.rle = rle;
    }
}
